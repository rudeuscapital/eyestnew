                // ascii log for this conversation has not been written yet
              {:else}
                // no conversation selected yet<br />
                // choose an episode from the sidebar to view its ascii log
              {/if}
            </p>
          </div>
        {/if}
      </div>
    </section>
  </div>
</main>

